<?php

declare(strict_types=1);

require_once __DIR__ . '/../vendor/autoload.php';

use Akunbeben\InlineConfirm\HoldToConfirm\HoldToConfirmMacros;
use Akunbeben\InlineConfirm\InlineConfirmation\InlineConfirmationMacros;

final class IdeStubGenerator
{
    private const ACTION_NAMESPACES = [
        'Filament\Actions',
        'Filament\Tables\Actions',
        'Filament\Infolists\Components\Actions',
    ];

    /** @var list<class-string> */
    private const MACRO_CLASSES = [
        InlineConfirmationMacros::class,
        HoldToConfirmMacros::class,
    ];

    public function generate(string $outputPath): void
    {
        $docblocks = $this->generateMethodDocblocks();
        $stubContent = $this->buildStubContent($docblocks);

        if (file_put_contents($outputPath, $stubContent) === false) {
            throw new RuntimeException(sprintf('Failed to write IDE stub to [%s].', $outputPath));
        }

        echo sprintf('IDE helper stubs generated successfully at %s%s', $outputPath, PHP_EOL);
    }

    private function generateMethodDocblocks(): string
    {
        $methods = [];

        foreach (self::MACRO_CLASSES as $macroClass) {
            $refClass = new ReflectionClass($macroClass);

            foreach ($refClass->getMethods(ReflectionMethod::IS_PUBLIC) as $method) {
                if ($method->getName() === 'actionMacro') {
                    continue;
                }

                if (! $method->isStatic()) {
                    continue;
                }

                /** @var Closure $closure */
                $closure = $method->invoke(null);
                $closureRef = new ReflectionFunction($closure);

                $paramsList = $this->buildParametersList($closureRef);
                $returnType = $this->determineReturnType($closureRef);

                $methods[] = sprintf('     * @method %s %s(%s)', $returnType, $method->getName(), $paramsList);
            }
        }

        return implode("\n", $methods);
    }

    private function buildParametersList(ReflectionFunction $closureRef): string
    {
        $params = [];

        foreach ($closureRef->getParameters() as $param) {
            $type = $param->getType();
            $typeStr = '';

            if ($type instanceof ReflectionNamedType) {
                $typeStr = $type->getName();
                if ($type->allowsNull() && $typeStr !== 'mixed') {
                    $typeStr = '?' . $typeStr;
                }

                $typeStr .= ' ';
            } elseif ($type instanceof ReflectionUnionType) {
                $types = array_map(fn (ReflectionIntersectionType | ReflectionNamedType $t) => $t->getName(), $type->getTypes());
                $typeStr = implode('|', $types) . ' ';
            }

            $paramStr = $typeStr . '$' . $param->getName();

            if ($param->isDefaultValueAvailable()) {
                $default = var_export($param->getDefaultValue(), true);
                $paramStr .= ' = ' . $default;
            }

            $params[] = $paramStr;
        }

        return implode(', ', $params);
    }

    private function determineReturnType(ReflectionFunction $closureRef): string
    {
        $returnType = $closureRef->getReturnType();

        if ($returnType && (string) $returnType === 'bool') {
            return 'bool';
        }

        return 'static';
    }

    private function buildStubContent(string $docblocks): string
    {
        $content = "<?php\n\n// This file is auto-generated by bin/ide.php. Do not edit it manually.\n";

        foreach (self::ACTION_NAMESPACES as $namespace) {
            $content .= <<<PHP

namespace {$namespace} {
    /**
{$docblocks}
     */
    class Action {}
}

PHP;
        }

        return $content;
    }
}

try {
    $generator = new IdeStubGenerator;
    $generator->generate(__DIR__ . '/../stubs/ide.php');
} catch (Throwable $throwable) {
    echo sprintf('Error generating stubs: %s%s', $throwable->getMessage(), PHP_EOL);
    exit(1);
}
